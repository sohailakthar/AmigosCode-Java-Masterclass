package com.sohail.M2_Conditionals;

public class Exercise5 {
    public static void main(String[] args) {
        // Create integer variables
        int firstVariable = 10;
        int secondVariable = 20;
        int thirdVariable = 30;

        // Check if firstVariable is greater than secondVariable and less than thirdVariable
        if (firstVariable > secondVariable && firstVariable < thirdVariable) {
            System.out.println("The first variable is greater than the second variable and less than the third variable.");
        } else {
            System.out.println("The first variable does not meet the specified condition.");
        }
    }
}
