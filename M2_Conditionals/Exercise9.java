package com.sohail.M2_Conditionals;

public class Exercise9 {
    public static void main(String[] args) {
        // Create a for loop that goes from 0 to 20
        for (int i = 0; i <= 20; i++) {
            // Check if the current value is even (divisible by 2)
            if (i % 2 == 0) {
                System.out.println(i); // Print the even value
            }
        }
    }
}
